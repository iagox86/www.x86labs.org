                    <span class="pageheader">Clan x86 Rules:</span>
                    <br />
                    <p><span class="sectionheader">King/Queen Figurehead</span>
                        <ul>
                            <li>Initially, Newby is the King of x86.</li>
                            <li>The king is a figurehead; he has no real power
                            to make decisions.</li>
                            <li>The king is in charge of doing stuff that
                            ordinary leaders can't, for example, passwords,
                            server maintenance, collecting votes.</li>
                            <li>If for any reason the King can no longer fulfill
                            his duty, his successor will be appointed and become
                            the new King with all the power and
                            responsibilities.</li>
                            <li>If the King will be unavailable for any period
                            of time, he can appoint a representative to take
                            over his position in the meantime.
                            The representative will have the same power and
                            duties as the king, and must step down when the King
                            returns.</li>
                            <li>If the King is unable to fulfil his duty, and
                            cannot appoint a successor (dead or imprisoned or
                            otherwise), a vote will be held for all members.
                            If the vote is a tie, and one of the potential
                            leaders has had previous leading experience, he will
                            be in charge.  Otherwise, the potential leaders will
                            flip a coin, best 2 out of 3.</li>
                            <li>The King cannot vote in elections.</li>
                            <li>The King may vote, as a normal member would, on
                            new recruits.</li>
                            <li>If a unanimous vote is made between members to
                            impeach the King, a new King will be voted on and
                            the current king must step down to be a normal
                            member or, by his own choice, leave the clan
                            completely.</li>
                        </ul>
                    </p>
                    <p><span class="sectionheader">Voting</span>
                        <ul>
                            <li>Unless otherwise stated by the specific rule, the
                            following rules comprise a single "vote".</li>
                            <li>All leaders and active members get a single vote
                            each.</li>
                            <li>The votes are submitted to the King, who keeps them
                            confidential.</li>
                            <li>Votes last for 2 weeks, during which the members are
                            responsible for talking to the king.</li>
                            <li>Voting is never required.</li>
                            <li>Results are shown only AFTER the vote has completed,
                            and results are to ONLY include the number of votes for
                            each choice, and there should be no way to associate a
                            vote with a specific person or group.</li>
                            <li>If a vote is ever a tie, the King will make the
                            deciding vote(s).  The tie and the King's decision will
                            be indicated in the results.</li>
                            <li>Votes are counted out of the total number of votes
                            not including people who don't vote.  So if there is a
                            decision among 15 members with 6 yes/3 no, the decision
                            will favour yes.</li>
                            <li>When an acceptance vote (ie, a yes/no to a change)
                            is necessary, a percentage must be specified in the rule
                            indicating the necessary ratio.  For example, removing a
                            leader requires a 60% decision and a new member joining
                            requires 75%.</li>
                        </ul>
                    </p>
                    <p><span class="sectionheader">Leaders</span>
                        <ul>
                            <li>There are initially 3 leaders.  If the clan has 40
                            - 69 members, there will be 5 leaders.  If the clan has
                            70 or more members, there will be 7 leaders.</li>
                            <li>If an election is held, and not enough to run the
                            clan run (less than 3, initially), they will all be made
                            leaders for the time being immediately.  When more
                            people are interested in running, the King should call
                            an election and the proper number of leaders can be
                            voted on.</li>
                            <li>Each member gets the number of votes equal to the
                            number of possible leaders, so if 3 leaders are being
                            elected, each member gets 3 votes.  These votes must be
                            for different people.  Normal voting rules apply
                            otherwise.</li>
                            <li>Any member may be a leader, if elected.</li>
                            <li>To be elected, a member must express interest in
                            being a potential leader.  If somebody doesn't wish to
                            be leader, they can't be voted for.</li>
                            <li>Elections are held every 4 months.</li>
                            <li>The King may "call" an election at any time.</li>
                            <li>First election will be short, 2 days.  Afterwards,
                            it is a standard 2-week vote.</li>
                            <li>Members don't have to use all their votes.</li>
                            <li>Members may not vote for themselves.</li>
                            <li>The King may not be a leader.</li>
                            <li>If a leader is being disruptive to the clan, a
                            vote may be held to remove them from leadership, per the
                            standard voting rules with a 75% approval required.  If
                            the vote passes, they will become an ordinary
                            member.</li>
                            <li>A leader may voluntarely step down and become a
                            member at any time.</li>
                            <li>If for any reason there is the incorrect number of
                            leaders:
                                <ul>
                                <li>if there are too few leaders, either because of
                                one stepping down, leaving, members joining, etc., a
                                normal election will be held to fill the
                                vacancies.</li>
                                <li>if there are too many leaders, a normal election
                                will be held and new leaders will be voted on.</li>
                                </ul>
                            </li>
                        </ul>
                    </p>
                    <p><span class="sectionheader">Members</span>
                        <ul>
                            <li>All members are considered equal.  There is no
                            concept of "seniority" or "novices".</li>
                            <li>Members may be in clans besides this one.</li>
                            <li>Members may be Active or Inactive.</li>
                            <li>Members are expected to be in the channel
                            occasionally, and to participate on forums
                            occasionally.</li>
                            <li>If a member is inactive either intentionally
                            (doesn't vote or spend time with the clan) or
                            unintentionally (away from the Internet for an
                            extended period), they will be placed on the
                            inactive list and will not participate in votes
                            until they return to activeness.</li>
                            <li>A member may request to be moved to the active
                            or inactive list at any time.</li>
                            <li>If a member is inactive for an extended period
                            of time, a leader may move them to the inactive
                            list.</li>
                            <li>If any member is being disruptive to the clan,
                            a vote will be held whether or not to remove them
                            from the clan, per the standard voting rules with
                            a 60% approval rating.</li>
                            <li>A member may leave the clan at any time.</li>
                            <li>A vote may be held (with 75% majority) to kick
                            a member out of the clan.</li>
                        </ul>
                    </p>
                    <p><span class="sectionheader">Channel</span>
                        <ul>
                            <li>Official channel is "op x86" on
                            uswest.battle.net.</li>
                            <li>Member accounts will be safelisted.</li>
                            <li>To have bot priviledges, members must wear clan
                            tags, [x86], after their name.</li>
                            <li>Members may not ban or kick other members for
                            any reason.</li>
                            <li>Members are asked to respect guests,
                            particularely if they are friends of a member.
                            However, in the end, clanmates are given priority
                            over guests in the channel.</li>
                            <li>If any member is, in the opinion of a leader,
                            abusing the bot, he will be warned to stop it.  If
                            he continues, the leader may remove his access
                            temporarely or permenantly (at the leader's
                            discression).</li>
                        </ul>
                    </p>
                    <p><span class="sectionheader">Joining</span>
                        <ul>
                            <li>Before a vote is made, a member must get
                            approval of at least 75% of leaders, rounded off. As
                            usual, the King will be notified when approval is
                            recieved and will take care of moving the procedure
                            on when appropriate leader approval is reached.</li>
                            <li>Leaders may take as long as they feel is
                            necessary to approve members.</li>
                            <li>After the leader approval is gained, a vote is
                            held whether or not to accept the member, per the
                            standard voting rules with an acceptance of 75%
                            required.</li>
                        </ul>
                    </p>
                    <p><span class="sectionheader">Forum / Website</span>
                        <ul>
                            <li>The website will be made by and for members.
                            Leaders will decide who gets to work on the site,
                            and how it should look.</li>
                            <li>Members are strongly encouraged to participate
                            on the forum, but it's not a requirement.</li>
                            <li>Members may request that boards be added to the
                            forum, and will be added at the discression of the
                            leaders.</li>
                            <li>Members may request to moderate certain boards,
                            and will be given moderator status at discression of
                            the leaders.</li>
                            <li>Moderation status may be removed at the
                            discression of the leaders.</li>
                            <li>There is a private section of the forum that
                            only members have access to.  Please keep any
                            discussions there private.</li>
                            <li>There is a private section of the forum that
                            only leaders have access to.  Keep in mind that,
                            because leaders change and the forum remains,
                            anybody can potentially have access to it
                            eventually.</li>
                        </ul>
                    </p>
                    <p><span class="sectionheader">Rules</span>
                        <ul>
                            <li>If there are any disputes on interpretation of
                            any of these rules, the originally intention will be
                            decided by the King and any ambiguity will be
                            cleaned up.</li>
                            <li>If a change to any rule is required, the new
                            rule will be proposed and a vote will be held that
                            requires 75% member approval.</li>
                            <li>If a rule should be added or removed, a leader
                            may propose a vote to do so.  A standard vote will
                            be held, requiring 75% member approval.  This rule
                            can't be removed for any reason, because that would
                            be too ironic and the universe might end.</li>
                        </ul>
                    </p>
                    <p><span class="sectionheader">Ammendment 1 - Clan Dispute Act</span>
                        <ul>
                            <li>All Clan Disputes in the Channel are to be done
                            via whispers, we don't need our Clan's problems
                            being public news.</li>
                            <li>All Clan Disputes in the Forums are to be kept
                            to Member Forums where leaders can deal with
                            them.</li>
                            <li>All Clan Disputes over IRC or any other Chatting
                            Programs are to be kept via PMs</li>
                            <li>If you break one of the above rules in addition
                            to breaking a channel rule
                            (ie kicking/banning members) a more severe
                            punishment will be given.</li>
                        </ul>
                    </p>
